import java.util.ArrayList;

class Searching {
    public static void main(String[] args) {
        ArrayList<Integer> list = new ArrayList<Integer>();

        list.add(15);
        list.add(61);
        list.add(2);
        list.add(13);
        list.add(27);

        for (int it = 0; it < list.size(); it++) {
            System.out.println(list.get(it) + " ");
        }
    }
}