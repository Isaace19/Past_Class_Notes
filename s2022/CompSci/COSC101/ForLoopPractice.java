class ForLoopPractice {
    public static void main(String[] args) {
        /* Incrementamenting */
        for (int i = 0; i < 5; i++) {
            System.out.println("Curently i is equal to " + i);
        }

        /* Decrementating */

        for (int j = 3; j > 0; j--) {
            System.out.println("Currently j is equal to " + j);
        }

        /* Other steps */
        for (int k = 1; k = 5; k += 2) {
            System.out.println("Currently k is equal to " + k);
        }
    }
}